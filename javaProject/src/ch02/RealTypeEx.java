package ch02;

public class RealTypeEx {

	public static void main(String[] args) {
		double d = 0.1234;
		double e = 1234E-4;
		float f = 0.1234F; //float 형은 숫자 뒤 f를 꼭 붙혀야됨
		double w = .1234D; 
		
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(w);
	}

}
