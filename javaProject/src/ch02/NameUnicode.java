package ch02;

public class NameUnicode {

	public static void main(String[] args) {
		char aValue = '\uae40';
		System.out.println("char형 (\'\\uae40\') : " + aValue);
		char bValue = '\uc6d0';
		System.out.println("char형 (\'\\uc6d0\') : " + bValue);
		char cValue = '\uc2dd';
		System.out.println("char형 (\'\\uc2dd\') : " + cValue);
	}

}
	